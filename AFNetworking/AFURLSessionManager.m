- (NSArray *)tasksForKeyPath:(NSString *)keyPath {
    __block NSArray *tasks = nil;
    // 1.新建一个信号
    dispatch_semaphore_t semaphore = dispatch_semaphore_create(0);
    [self.session getTasksWithCompletionHandler:^(NSArray *dataTasks, NSArray *uploadTasks, NSArray *downloadTasks) {
        if ([keyPath isEqualToString:NSStringFromSelector(@selector(dataTasks))]) {
            tasks = dataTasks;
        } else if ([keyPath isEqualToString:NSStringFromSelector(@selector(uploadTasks))]) {
            tasks = uploadTasks;
        } else if ([keyPath isEqualToString:NSStringFromSelector(@selector(downloadTasks))]) {
            tasks = downloadTasks;
        } else if ([keyPath isEqualToString:NSStringFromSelector(@selector(tasks))]) {
            tasks = [@[dataTasks, uploadTasks, downloadTasks] valueForKeyPath:@"@unionOfArrays.self"];
            // @distinctUnionOfObjects 清除重复值
            // unionOfObjects 保留重复值
        }
        // 2.在异步方法中发送信号，也就说一旦我们得到了异步的结果，我们就发一个信号
        dispatch_semaphore_signal(semaphore);
    }];
    // 3.等待信号，只有接收到指定的信号代码才会往下走
    dispatch_semaphore_wait(semaphore, DISPATCH_TIME_FOREVER);
    /**
     * 这个信号的使用场景有很多，可以当安全锁来使用，也可以像上边一样异步等待。 假如我们有这样一个场景：我们有3个或者多个异步的网络请求，必须等待所有的请求回来后，在使用这些请求的结果来做一些事情。那么该怎么办呢？ 解决方案就是：使用dispatch_group_t 和 dispatch_semaphore_t来实现。 在这里代码就不贴出来了
     */
    return tasks;
}
// *** getTasksWithCompletionHandler 这个方法是异步方法，上边的方法中我们需要等待这个异步方法有结果后才能进行后边的代码。 我们就可以使用dispatch_semaphore_t 这个信号来实现异步等待。


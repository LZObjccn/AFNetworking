static dispatch_queue_t url_session_manager_creation_queue() {
    static dispatch_queue_t af_url_session_manager_creation_queue;
    static dispatch_once_t onceToken;
    dispatch_once(&onceToken, ^{
        af_url_session_manager_creation_queue = dispatch_queue_create("com.alamofire.networking.session.manager.creation", DISPATCH_QUEUE_SERIAL);
    });

    return af_url_session_manager_creation_queue;
}
/**
 * AFNetworking中所有的和创建任务相关的事件都放到了一个单例的队列中，我们平时可能会使用这些方法，但还是可能会忽略一些内容，dispatch_queue_create()这个是队列的方法，第一个参数是队列的identifier，第二个参数则表示这个队列是串行队列还是并行队列。
 * 如果第二个参数为DISPATCH_QUEUE_SERIAL或NULL 则表示队列为串行队列。如果为DISPATCH_QUEUE_CONCURRENT则表示是并行队列
 */

static void url_session_manager_create_task_safely(dispatch_block_t _Nonnull block) {
    if (block != NULL) {
        if (NSFoundationVersionNumber < NSFoundationVersionNumber_With_Fixed_5871104061079552_bug) {
            // Fix of bug
            // Open Radar:http://openradar.appspot.com/radar?id=5871104061079552 (status: Fixed in iOS8)
            // Issue about:https://github.com/AFNetworking/AFNetworking/issues/2093
            dispatch_sync(url_session_manager_creation_queue(), block);
        } else {
            block();
        }
    }
}
/**
 * 再看这个方法，看名字能够知道这应该是一个安全创建任务的方法，那么我们会很疑惑，为什么创建任务要是安全的呢？难道我们按照顺序创建任务，根据各自的Block回调处理事件会有问题？？
 * 假如我们创建了一个任务task1 对应completionHandler1，然后又创建了task2 对应的completionHandler2，这时候在task2数据还没有返回的前提下，task1的数据返回了，就会调用completionHandler2，就是这样的一个bug，造成任务的创建是不安全的，不过这个问题已经在ios8后修复了。
 */
/**
 * typedef void (^dispatch_block_t)(void);
 * 关于这个Block我们应该注意几点:
 * 非ARC情况下，Block被allocated或者copied到堆后，一定要记得释放它，通过[release]或者Block_release()
 * 声明Block时，它是被分配到栈上的，要使用他，需要copy到堆才安全，因为栈内存是系统管理的，随时可能被释放。
 */

static dispatch_queue_t url_session_manager_processing_queue() {
    static dispatch_queue_t af_url_session_manager_processing_queue;
    static dispatch_once_t onceToken;
    dispatch_once(&onceToken, ^{
        af_url_session_manager_processing_queue = dispatch_queue_create("com.alamofire.networking.session.manager.processing", DISPATCH_QUEUE_CONCURRENT);
    });

    return af_url_session_manager_processing_queue;
}
// *** 这个方法是创建一个队列用来管理数据的处理。和上边的创建的方法对比，这个方法创建的队列是一个并行的队列，这就加快了数据的处理速度。

